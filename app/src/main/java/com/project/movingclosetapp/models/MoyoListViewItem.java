package com.project.movingclosetapp.models;

public class MoyoListViewItem {

    private String moyoName;
    private String moyoAddr;
    private String moyoStartEnd;
    private String moyoDday;
    private String moyoStatus;

    public String getMoyoName() {
        return moyoName;
    }

    public void setMoyoName(String moyoName) {
        this.moyoName = moyoName;
    }

    public String getMoyoAddr() {
        return moyoAddr;
    }

    public void setMoyoAddr(String moyoAddr) {
        this.moyoAddr = moyoAddr;
    }

    public String getMoyoStartEnd() {
        return moyoStartEnd;
    }

    public void setMoyoStartEnd(String moyoStartEnd) {
        this.moyoStartEnd = moyoStartEnd;
    }

    public String getMoyoDday() {
        return moyoDday;
    }

    public void setMoyoDday(String moyoDday) {
        this.moyoDday = moyoDday;
    }

    public String getMoyoStatus() {
        return moyoStatus;
    }

    public void setMoyoStatus(String moyoStatus) {
        this.moyoStatus = moyoStatus;
    }
}
